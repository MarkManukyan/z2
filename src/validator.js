class Validator {
  validateUsername(username) {
    // Проверка: только допустимые символы (латинские буквы, цифры, тире, подчёркивания)
    const validChars = /^[a-zA-Z0-9_-]+$/;
    if (!validChars.test(username)) {
      throw new Error('Недопустимые символы в имени пользователя');
    }

    // Проверка: не начинается с цифры, тире или подчёркивания
    if (/^[0-9_-]/.test(username)) {
      throw new Error('Имя не может начинаться с цифры, тире или подчёркивания');
    }

    // Проверка: нет более трёх цифр подряд
    if (/(\d){4,}/.test(username)) {
      throw new Error('Нельзя использовать более трёх цифр подряд');
    }

    // Проверка: не заканчивается тире или подчёркиванием
    // Цифры в конце допустимы, если их не более трёх подряд
    if (/[-_]$/.test(username)) {
      throw new Error('Имя не может заканчиваться тире или подчёркиванием');
    }

    return true;
  }
}

module.exports = Validator;
